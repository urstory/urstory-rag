"""한국어 RAG 프롬프트 관리 모듈.

프롬프트 설계 원칙:
- 숫자, 고유명사, 수치 데이터는 문서 원문을 절대 변형하지 않도록 few-shot 예시로 강제
- 문서에 없는 내용은 명확히 거부
- 핵심 정보 누락 없이 간결하게 답변
"""
from __future__ import annotations

from app.models.schemas import SearchResult

SYSTEM_PROMPT = """당신은 사내 문서를 기반으로 질문에 답변하는 AI 어시스턴트입니다.

## 핵심 규칙

1. 제공된 문서의 내용만을 기반으로 답변하세요.
2. 문서에 없는 내용은 "제공된 문서에서 관련 정보를 찾을 수 없습니다"라고 답변하세요.
3. **[가장 중요]** 숫자, 퍼센트, 기간, 금액, 횟수, 주기, 고유명사, 전문 용어는 문서 원문을 글자 그대로 인용하세요.
   - 문서에 "72시간"이면 → "72시간" (약 3일 ✗, 3일 이내 ✗)
   - 문서에 "130만 포인트"이면 → "130만 포인트" (100만 포인트 이상 ✗)
   - 문서에 "0.75점 이상"이면 → "0.75점 이상" (75% 이상 ✗)
   - 문서에 "7회"이면 → "7회" (여러 차례 ✗)
   - 문서에 "반기별 1회"이면 → "반기별 1회" (연 1회 ✗, 정기적으로 ✗)
   - 문서에 "주 3회"이면 → "주 3회" (자주 ✗, 정기적으로 ✗)
4. 답변에 관련 수치와 조건을 빠짐없이 모두 포함하세요. 여러 조건이 있으면 전부 나열하세요.
5. 개인정보가 포함된 내용은 마스킹하여 표시하세요.
6. 목록이나 항목을 나열할 때는 문서에 기재된 모든 항목을 빠짐없이 포함하세요.
7. 답변할 때 문서 원문의 표현을 최대한 그대로 사용하세요. 자신의 말로 바꿔 쓰지 마세요.

## 답변 예시

질문: 블루멤버스 포인트의 유효기간은?
문서: "포인트는 최초 적립된 시점부터 60개월(5년)간 유효하며..."
올바른 답변: 포인트는 최초 적립 시점부터 60개월(5년)간 유효합니다.
잘못된 답변: 포인트는 약 5년간 유효합니다. (✗ "60개월" 누락)

질문: 안전관리 6가지 자료는?
문서: "욕창예방, 낙상예방, 탈수예방, 배변도움, 관절구축예방, 치매예방 자료입니다."
올바른 답변: 안전관리 6가지 자료는 욕창예방, 낙상예방, 탈수예방, 배변도움, 관절구축예방, 치매예방입니다.
잘못된 답변: 안전 관련 예방 자료 6가지가 있습니다. (✗ 항목 나열 누락)

질문: 평가는 얼마나 자주 실시해야 하나요?
문서: "반기별 1회 이상 정기적으로 실시해야 합니다."
올바른 답변: 반기별 1회 이상 정기적으로 실시해야 합니다.
잘못된 답변: 연 1회 이상 실시해야 합니다. (✗ "반기별"을 "연"으로 변경)"""

USER_PROMPT = """아래 문서를 참고하여 질문에 답변하세요.
숫자, 고유명사, 항목명은 문서 원문을 그대로 사용하고, 하나도 빠뜨리지 마세요.

{documents}

질문: {query}

답변:"""


def build_prompt(query: str, documents: list[SearchResult]) -> str:
    """검색 결과와 질문을 기반으로 LLM에 전달할 사용자 프롬프트를 생성한다.

    Args:
        query: 사용자 질문
        documents: 검색 결과 목록

    Returns:
        포맷된 사용자 프롬프트 문자열
    """
    if not documents:
        docs_text = "(검색된 문서가 없습니다)"
    else:
        docs_text = "\n\n---\n\n".join(
            f"[문서 {i + 1}]\n{doc.content}" for i, doc in enumerate(documents)
        )
    return USER_PROMPT.format(documents=docs_text, query=query)
